
using AdoptHub.Crm.Domain.Services.Features.Instances.Commands.CreateInstance;
using AdoptHub.Crm.Infrastructure.Kubernetes;

namespace AdoptHub.Crm.Api;

public class Program
{
    public static void Main(string[] args)
    {
        var builder = WebApplication.CreateBuilder(args);
        builder.AddServiceDefaults();

        // Add services to the container.

        builder.Services.AddControllers();
        // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
        builder.Services.AddEndpointsApiExplorer();
        builder.Services.AddSwaggerGen();

        builder.Services.AddSingleton<IKubernetesDeploymentBuilder, KubernetesDeploymentBuilder>();

        builder.Services.AddMediatR(cfg => cfg.RegisterServicesFromAssemblyContaining<CreateInstanceCommandHandler>());

        var app = builder.Build();

        app.MapDefaultEndpoints();

        // Configure the HTTP request pipeline.
        if (app.Environment.IsDevelopment())
        {
            app.UseSwagger();
            app.UseSwaggerUI();
        }

        app.UseHttpsRedirection();

        app.UseAuthorization();


        app.MapControllers();

        app.Run();
    }
}
